CREATE TABLE FLOWER(
		FLOWERNAME VARCHAR2(200),
	COLOR VARCHAR2(100),
	PRICE NUMBER,
	CONSTRAINT FLOWER_PK PRIMARY KEY(FLOWERNAME)
);
