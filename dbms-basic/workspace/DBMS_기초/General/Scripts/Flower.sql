CREATE TABLE POT(
 	POTID VARCHAR2(100),
 	POTCOLOR VARCHAR2(200),
 	POTSHAPE VARCHAR2(200),
 	FLOWERNAME VARCHAR2(200),
 	CONSTRAINT POT_PK PRIMARY KEY(POTID),
 	CONSTRAINT POT_FK FOREIGN KEY(FLOWERNAME) REFERENCES FLOWER(FLOWERNAME)
);

